// ********* MISE EN PLACE DE NOTRE SERVEUR HTTP **********

//  on importe un protocole HTTP
const http = require('http');

// on importe notre app qui sera utilisée sur le serveur HTTP
const app = require('./app');

// on met en place l'utilisation du port 3000 sur le serveur HTTP
const normalizePort = val => {
  const port = parseInt(val, 10);

  if (isNaN(port)) {
    return val;
  }
  if (port >= 0) {
    return port;
  }
  return false;
};
const port = normalizePort(process.env.PORT || '3000');
app.set('port', port);

// on met en place la gestion des erreurs sur le serveur HTTP 
// et on créer notre serveur HTTP qui héberge notre app.
const errorHandler = error => {
  if (error.syscall !== 'listen') {
    throw error;
  }
  const address = server.address();
  const bind = typeof address === 'string' ? 'pipe ' + address : 'port: ' + port;
  switch (error.code) {
    case 'EACCES':
      console.error(bind + ' requires elevated privileges.');
      process.exit(1);
      break;
    case 'EADDRINUSE':
      console.error(bind + ' is already in use.');
      process.exit(1);
      break;
    default:
      throw error;
  }
};

const server = http.createServer(app);

server.on('error', errorHandler);
server.on('listening', () => {
  const address = server.address();
  const bind = typeof address === 'string' ? 'pipe ' + address : 'port ' + port;
  console.log('Listening on ' + bind);
});

// on met en place l'écoute du port 3000 sur le serveur HTTP
server.listen(port);